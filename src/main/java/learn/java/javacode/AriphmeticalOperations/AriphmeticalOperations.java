package learn.java.javacode.AriphmeticalOperations;

public class AriphmeticalOperations {
	public static void main(String[] args) {
		int k = 42;
		double j = 42.5;
		
//			k=k*k;
			System.out.println(k*=k);
			
//			j/10=2.5				
			System.out.println(k%10);
			System.out.println(j%10);

//			k+1=k;
			System.out.println(++k);
	
//			k=k+k;
			System.out.println(k+=k);
			
//			k=k-k;
			System.out.println(k-=k);
			
	}
}
